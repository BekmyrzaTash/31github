public class Loops1 {
    public static void main(String[]args) {
        int i =0;
                while(i<7){
                    System.out.println("Cycle while"+i);
                    i++;
                }
                System.out.println();
                for (int j=0; j<7; j++){
                    System.out.println("Cycle for: " + j);
                }
    }
}
